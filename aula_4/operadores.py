# Operadores Matemáticos  
# 1. + Soma
# 2. - subtração
# 3. * multiplicação
# 4. / divisão
# 5. ** potência
# 6. % módulo

numero_um = 20
numero_dois = 40
soma = numero_um + numero_dois
print(f"A soma entre {numero_um} + {numero_dois} é {soma}")

numero_tres = 1
numero_quatro = 2

subtracao = numero_tres - numero_quatro

print (f"A subtração entre {numero_tres} - {numero_quatro} é {subtracao}")

numero_cinco = 100
numero_seis = 5

multiplicacao = numero_cinco * numero_seis
print(f"A multiplicação entre {numero_cinco} * {numero_seis} é {multiplicacao}")

numero_sete = 50
numero_oito = 10

divisao = numero_sete / numero_oito
print(f"A divisão entre {numero_sete} / {numero_oito} é {divisao}")




